# ActiveCode 3 (chp08_index1): example on string indexing with []

school = "Luther College"
m = school[2] # don't forget to start with index 0!
print(m)

lastchar = school[-1]   # -1 is last char: think index 0 (first)
                        #   wrapped around to last
print(lastchar)
